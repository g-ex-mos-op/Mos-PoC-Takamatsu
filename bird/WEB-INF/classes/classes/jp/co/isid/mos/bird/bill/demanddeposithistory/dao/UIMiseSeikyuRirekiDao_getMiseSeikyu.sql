SELECT 
    BS01.MISE_CD
   ,BM01.MISE_NAME_KJ
   ,sum(BS01.TAXY_KIN) as TAXY_KIN
   ,sum(BS01.TAXN_KIN) as TAXN_KIN
   ,sum(BS01.KOMI_KIN) as KOMI_KIN
   ,sum(BS01.UCHI_TAX) as UCHI_TAX
FROM 
    BS01URSR BS01 LEFT OUTER JOIN BM01TENM BM01 ON 
    BS01.COMPANY_CD = BM01.COMPANY_CD AND BS01.MISE_CD = BM01.MISE_CD
WHERE
      BS01.COMPANY_CD = /*companyCd*/'00'
  AND BS01.URIKAKE_CD = /*urikakeCd*/'36056'
  AND BS01.SEIKYUSHO_ID = /*seikyushoId*/'2006073101'
  AND (BS01.TAXN_KIN != 0 OR BS01.TAXY_KIN != 0 OR BS01.KOMI_KIN != 0) 
GROUP BY
    BS01.MISE_CD
   ,BM01.MISE_NAME_KJ
