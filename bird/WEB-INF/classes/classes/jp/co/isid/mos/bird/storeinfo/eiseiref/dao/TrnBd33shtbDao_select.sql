SELECT BD33.BNFC_YEAR
,      BD33.COMPANY_CD
,      BD33.MISE_CD
,      BM01.MISE_NAME_KJ
,      BM10.SIBU_CD
,      BM10.SIBU_NAME
,      BD33.MACHINE1_NAME
,      BD33.MACHINE2_NAME
,      BD33.GRIDOLE1_NAME
,      BD33.GRIDOLE2_NAME
,      BD33.FRYER1_NAME
,      BD33.FRYER2_NAME
,      BD33.FRYER3_NAME
,      BD33.WATER_KIND
,      BD33.TYPE_START_DATE
,      BD33.TYPE_NM
,      BD33.PDF_FILENM
,      BD33.KAKU_DATE
,      BD33.TENSO_DATE

FROM BD33SHTB BD33
,    BM01TENM BM01
,    BM10GSIB BM10

WHERE  BD33.BNFC_YEAR  = /*nendo*/'2013'
AND    BD33.COMPANY_CD = /*companyCd*/'00'
AND    BD33.MISE_CD    = /*miseCd*/'00018'
AND    BD33.COMPANY_CD = BM01.COMPANY_CD 
AND    BD33.MISE_CD    = BM01.MISE_CD
AND    BM01.COMPANY_CD = BM10.COMPANY_CD 
AND    BM01.SIBU_CD    = BM10.SIBU_CD
/*IF limitFlg && "01".equals(userTypeCd) */
AND    EXISTS(SELECT BM01.SIBU_CD, BM01.MISE_CD
             FROM BM01TENM BM01
             ,    BM51SVSB BM51
             WHERE BM51.SV_CD      = /*userId*/'99990003'
             AND   BM01.COMPANY_CD = BM51.COMPANY_CD 
             AND   BD33.COMPANY_CD = BM01.COMPANY_CD 
             AND   BM01.SIBU_CD    = BM51.SIBU_CD
             AND   BD33.MISE_CD    = BM01.MISE_CD)
/*END*/
