SELECT
	BM06UONR.USER_ID,
	BM06UONR.COMPANY_CD,
	BM11ONER.ONER_NAME_KJ,
	BM06UONR.ONER_CD,
	BM06UONR.FIRST_USER,
	BM06UONR.FIRST_PGM,
	BM06UONR.FIRST_TMSP,
	BM06UONR.LAST_USER,
	BM06UONR.LAST_PGM,
	BM06UONR.LAST_TMSP
FROM 
    BM06UONR LEFT JOIN 
    BM11ONER ON (BM06UONR.COMPANY_CD= BM11ONER.COMPANY_CD AND BM06UONR.ONER_CD= BM11ONER.ONER_CD)
WHERE BM06UONR.USER_ID = /*user_id*/'99990007'
ORDER BY BM06UONR.USER_ID