SELECT
    BD62.TMSP,
	BD62.NENDO,
	RTRIM(BD62.USER_ID) AS USER_ID,
	BD62.TORI_SAKUJO_FLG,
	BD62.POINT,
	BD62.POINT_SHU,
	BD62.KB_COMPANY_CD,
	BD62.RANK_CD,
	RTRIM(BD62.KAIGAI_FLG) AS KAIGAI_FLG,
	RTRIM(BD62.BIKOU) AS BIKOU,
	BD62.FIRST_USER,
	BD62.FIRST_PGM,
	BD62.FIRST_TMSP,
	BD62.LAST_USER,
	BD62.LAST_PGM,
	BD62.LAST_TMSP

FROM
    BD62HPRI BD62
    LEFT OUTER JOIN BM77HUYP BM77
        ON (BD62.NENDO = BM77.NENDO
        	AND BD62.KB_COMPANY_CD = BM77.KB_COMPANY_CD
            AND BD62.RANK_CD = BM77.RANK_CD
            AND BM77.SAKUJO_FLG ='0')

WHERE BD62.TMSP = /*tmsp*/'2017-02-23 17:41:09.144000'
	AND BM77.NENDO IS NULL