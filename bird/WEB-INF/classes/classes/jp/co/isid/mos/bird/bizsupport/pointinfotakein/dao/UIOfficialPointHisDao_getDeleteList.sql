SELECT
	BD60.NENDO,
	RTRIM(BD60.USER_ID) AS USER_ID,
	BD60.POINT,
	BD60.POINT_SHU,
	RTRIM(BD60.POINT_SHU_NAME) AS POINT_SHU_NAME,
	BD60.KB_COMPANY_CD,
	RTRIM(BD60.BIKOU) AS BIKOU,
	BD60.FIRST_USER,
	BD60.FIRST_PGM,
	BD60.FIRST_TMSP,
	BD60.LAST_USER,
	BD60.LAST_PGM,
	BD60.LAST_TMSP

FROM
    BD60YPRI BD60
    INNER JOIN BD63YPRI BD63
        ON (BD63.NENDO = BD60.NENDO
            AND BD63.USER_ID = BD60.USER_ID
            AND BD63.POINT_SHU_NAME = BD60.POINT_SHU_NAME)

WHERE BD63.TORI_SAKUJO_FLG = '1'
    AND BD63.TMSP = /*tmsp*/'2017-02-23 17:41:09.144000'