SELECT 
	BR01.USER_ID,
	BR01.USER_NAME_KJ,
	BR01.USER_NAME_KANA,
	BR01.USERTYPE_CD,
	BR01.BUMON_CD,
	BC08.BUMON_NAME,
	BR01.USER_PSWD,
	BR01.PSWD_UPDT_DT,
	BR01.LAST_PSWD,
	BR01.SEKYU_FLG,
	BR01.MAIL_ADD,
	BR01.APPLIED_DT,
	BR01.SEKYU_DT,
	BR01.SEKYU_UPDT_DT,
	BR01.LIMIT_KBN,
	BR01.STOP_FLG

FROM  
   BR01USER BR01 LEFT JOIN 
    (select BC08.BUMON_CD, BC08.BUMON_NAME from BM03USCP BM03, BC08CBMN BC08 
    where BM03.USER_ID = /*user_id*/'99990003' 
        and BM03.R_COMPANY_CD = BC08.R_COMPANY_CD 
        and BM03.ZOKUSEI_KBN = '1') BC08 on (BR01.BUMON_CD = BC08.BUMON_CD)  
WHERE
    BR01.USER_ID = /*user_id*/'99990003' 
ORDER BY BR01.USER_ID	