SELECT
	BD64.TMSP,
	BD64.NENDO,
	RTRIM(BD64.USER_ID) AS USER_ID,
	BD64.TORI_SAKUJO_FLG,
	BD64.POINT_RUIKEI,
	BD64.KB_COMPANY_CD,
	BD64.NYUSYA_DT,
	BD64.TAISHOKU_DT,
	BD64.KINZOKU_YEAR,
	BD64.RETIRE_CD,
	BD64.SIKYU_RATE,
	BD64.T_SEISAN_POINT,
	RTRIM(BD64.BIKOU) AS BIKOU,
	BD64.FIRST_USER,
	BD64.FIRST_PGM,
	BD64.FIRST_TMSP,
	BD64.LAST_USER,
	BD64.LAST_PGM,
	BD64.LAST_TMSP

FROM
    BD64TSES BD64
    LEFT OUTER JOIN VIR51USER VIR51
        ON (BD64.USER_ID = VIR51.USER_ID)

WHERE BD64.TMSP = /*tmsp*/'2017-02-23 17:41:09.144000'
	AND VIR51.USER_ID IS NULL