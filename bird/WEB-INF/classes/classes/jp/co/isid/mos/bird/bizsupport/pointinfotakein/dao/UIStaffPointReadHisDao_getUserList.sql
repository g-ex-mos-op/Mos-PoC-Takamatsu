SELECT
	BD62.TMSP,
	BD62.NENDO,
	RTRIM(BD62.USER_ID) AS USER_ID,
	BD62.TORI_SAKUJO_FLG,
	BD62.POINT,
	BD62.POINT_SHU,
	BD62.KB_COMPANY_CD,
	BD62.RANK_CD,
	RTRIM(BD62.KAIGAI_FLG) AS KAIGAI_FLG,
	RTRIM(BD62.BIKOU) AS BIKOU,
	BD62.FIRST_USER,
	BD62.FIRST_PGM,
	BD62.FIRST_TMSP,
	BD62.LAST_USER,
	BD62.LAST_PGM,
	BD62.LAST_TMSP

FROM
    BD62HPRI BD62
    LEFT OUTER JOIN VIR51USER VIR51
        ON (BD62.USER_ID = VIR51.USER_ID)

WHERE BD62.TMSP = /*tmsp*/'2017-02-23 17:41:09.144000'
	AND VIR51.USER_ID IS NULL