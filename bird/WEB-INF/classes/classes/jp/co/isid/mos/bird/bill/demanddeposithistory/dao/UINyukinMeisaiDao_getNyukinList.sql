SELECT 
    BT36.COMPANY_CD
   ,BT36.URIKAKE_CD
   ,rtrim(TM11.ONER_NAME_KJ) as ONER_NAME_KJ
   ,BT36.URIKAKE_YM
   ,BT36.NYUKIN_DT
   ,BT36.NKANRI_NO
   ,BT36.DATA_VER
   ,BT36.DENPYO_NO
   ,BT36.NYUKIN_GAKU
   ,BT36.NYUKIN_KBN
   ,BT36.SAKUJO_FLG
   ,BT36.FIRST_USER
   ,BT36.FIRST_PGM
   ,BT36.FIRST_TMSP
   ,BT36.LAST_USER
   ,BT36.LAST_PGM
   ,BT36.LAST_TMSP
   ,BT36.DENPYO_KBN
   ,BT36.BANK_CD
   ,BT36.SITEN_CD
   ,BT36.YOKIN_SHU
   ,BT36.KOUZA_NO
   ,BT36.DENPYO_DT
   ,BT36.KENSHU_DT
FROM 
    BT36NYUT BT36
   ,BM33ONUR BM33
      LEFT OUTER JOIN TM11ONER TM11 ON (BM33.COMPANY_CD = TM11.COMPANY_CD AND BM33.URIKAKE_CD = TM11.ONER_CD)
WHERE
      BT36.COMPANY_CD =BM33.COMPANY_CD
  AND BT36.URIKAKE_CD =BM33.URIKAKE_CD
  AND BT36.COMPANY_CD = /*companyCd*/'00'
  AND BM33.ONER_CD = /*onerCd*/'36056'
  AND substr(BT36.NYUKIN_DT,1,6) >= /*startYm*/'200601'
  AND substr(BT36.NYUKIN_DT,1,6) <= /*endYm*/'200612'
  AND BT36.NYUKIN_GAKU != 0
  AND BT36.SAKUJO_FLG != '1'
ORDER BY
    BT36.URIKAKE_CD
   ,BT36.NYUKIN_DT
   ,BT36.NKANRI_NO
