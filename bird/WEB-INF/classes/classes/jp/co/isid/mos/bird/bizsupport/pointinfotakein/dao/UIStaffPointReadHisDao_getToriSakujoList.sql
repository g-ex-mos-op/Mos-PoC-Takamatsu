SELECT
	BD62.TMSP,
	BD62.NENDO,
	RTRIM(BD62.USER_ID) AS USER_ID,
	BD62.TORI_SAKUJO_FLG,
	BD62.POINT,
	BD62.POINT_SHU,
	BD62.KB_COMPANY_CD,
	BD62.RANK_CD,
	RTRIM(BD62.KAIGAI_FLG) AS KAIGAI_FLG,
	RTRIM(BD62.BIKOU) AS BIKOU,
	BD62.FIRST_USER,
	BD62.FIRST_PGM,
	BD62.FIRST_TMSP,
	BD62.LAST_USER,
	BD62.LAST_PGM,
	BD62.LAST_TMSP

FROM
    BD62HPRI BD62
    LEFT OUTER JOIN BD59HPRI BD59
        ON (BD62.NENDO = BD59.NENDO
            AND BD62.USER_ID = BD59.USER_ID
            AND BD62.POINT_SHU = BD59.POINT_SHU)

WHERE BD62.POINT_SHU = /*pointShu*/'1'
    AND BD62.TMSP = /*tmsp*/'2017-02-23 17:41:09.144000'
    AND BD62.TORI_SAKUJO_FLG = '1'
	AND BD59.NENDO IS NULL