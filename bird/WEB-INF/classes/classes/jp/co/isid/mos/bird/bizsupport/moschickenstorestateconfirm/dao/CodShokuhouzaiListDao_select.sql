SELECT BM41.CKANRI_NO
,      BM41.SHOKU_CD
,      RTRIM(PM06.SHOKU_NAME_KNA) AS SHOKU_NAME_KNA
FROM BM41CMNU BM41
,    PM06SHOK PM06
WHERE BM41.CKANRI_NO = /*ckanriNo*/'200636'
AND   BM41.SHOKU_CD  = PM06.SHOKU_CD
GROUP BY BM41.CKANRI_NO
,        BM41.SHOKU_CD
,        RTRIM(PM06.SHOKU_NAME_KNA)
ORDER BY BM41.SHOKU_CD