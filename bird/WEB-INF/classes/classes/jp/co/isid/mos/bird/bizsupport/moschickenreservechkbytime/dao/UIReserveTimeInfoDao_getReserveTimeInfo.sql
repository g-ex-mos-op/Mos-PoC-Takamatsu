select min(SUB1.RESERVE_HH || SUB1.RESERVE_MM) as RESERVE_STATE_TIME,
        max(SUB1.RESERVE_HH || SUB1.RESERVE_MM) as RESERVE_END_TIME
from
	BM40CMGP as BM40,
	BM41CMNU as BM41 left outer join
	(select 
		BT70.CKANRI_NO,
		BT71.MENU_CD ,	
		BT70.RESERVE_DT ,
		BT70.RESERVE_HH ,
		BT70.RESERVE_MM ,
		BT71.RESERVE_AMT
	 from 
	  	BT70CRSV as BT70,
		BT71CRSD as BT71
	 where 
			BT70.COMPANY_CD = '00' and
			BT70.CKANRI_NO = /*ckanriNo*/'200601' and
			BT70.CANCEL_FLG = '0' and
			BT70.MISE_CD = /*miseCd*/'01776' and
			BT70.RESERVE_DT = /*reservDt*/'20060502' and
			BT70.CKANRI_NO = BT71.CKANRI_NO and
			BT70.COMPANY_CD = BT71.COMPANY_CD and
			BT70.MISE_CD = BT71.MISE_CD and
			BT70.SEQ_NO = BT71.SEQ_NO
	) SUB1
	on BM41.CKANRI_NO = SUB1.CKANRI_NO and BM41.MENU_CD = SUB1.MENU_CD
, BM38MMNU as BM38
where 
	BM40.CKANRI_NO =  /*ckanriNo*/'200601' and
	BM40.CKANRI_NO = BM41.CKANRI_NO and 
	BM40.MENU_GROUP = BM41.MENU_GROUP and 
	BM38.COMPANY_CD = /*companyCd*/'00' and
	BM38.MISE_CD =  /*miseCd*/'01776' and
	BM41.MENU_CD = BM38.MENU_CD 