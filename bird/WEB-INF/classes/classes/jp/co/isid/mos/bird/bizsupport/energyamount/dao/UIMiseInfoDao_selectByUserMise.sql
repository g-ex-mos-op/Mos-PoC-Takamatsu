SELECT BM07.COMPANY_CD
,      BM07.MISE_CD
,      BM01.MISE_NAME_KJ

from BM01TENM BM01
,    BM07UTEN BM07
,    BR01USER BR01 
WHERE BM07.USER_ID = /*userId*/'99990003'
/*IF companyCd != null */
AND   BM07.COMPANY_CD = /*companyCd*/'00'
/*END*/
AND   BM07.COMPANY_CD = BM01.COMPANY_CD
AND   BM07.MISE_CD = BM01.MISE_CD
AND   BM07.USER_ID = BR01.USER_ID
order by BM07.COMPANY_CD
,        BM07.MISE_CD
