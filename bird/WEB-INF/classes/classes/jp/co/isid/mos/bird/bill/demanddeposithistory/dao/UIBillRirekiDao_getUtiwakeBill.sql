SELECT 
    BS02.COMPANY_CD
   ,BS02.URIKAKE_CD
   ,rtrim(TM11.ONER_NAME_KJ) as ONER_NAME_KJ
   ,BS02.HAKKO_DT
   ,BS02.SEIKYUSHO_ID
   ,BS02.SEIKYU_ZEN
   ,BS02.NYUKIN_GAKU
   ,BS02.KURI_ZAN
   ,BS02.KAZEI_GAKU
   ,BS02.SHO_TAX
   ,BS02.HAND_TAX
   ,(BS02.SHO_TAX + BS02.HAND_TAX)  as TAX_KEI   
   ,BS02.HIZEI_GAKU
   ,BS02.SEIKYU_KON
   ,BS02.NYUKIN_RUISEKI
   ,(BS02.KAZEI_GAKU + BS02.HIZEI_GAKU + BS02.SHO_TAX + BS02.HAND_TAX) as KAIAGE_GAKU 
FROM 
    BS02SEKR BS02
   ,TM11ONER TM11 
WHERE
      BS02.COMPANY_CD =TM11.COMPANY_CD
  AND BS02.URIKAKE_CD =TM11.ONER_CD 
  AND BS02.COMPANY_CD = /*companyCd*/'00'
  AND BS02.URIKAKE_CD = /*urikakeCd*/'36056'
  AND BS02.SEIKYUSHO_ID = /*seikyushoId*/'2005063001'
