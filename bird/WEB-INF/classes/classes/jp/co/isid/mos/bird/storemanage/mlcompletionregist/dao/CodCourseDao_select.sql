select rtrim(BR19.COURSE_CD) as COURSE_CD
,	   rtrim(BR19.COURSE_NAME) as COURSE_NAME

from BC10ENTM BC10
,    BR17ENTL BR17
,    BR18ENTD BR18
,    BR19MLCR BR19

where BC10.ENTRY_TYPE='1'
AND   BR18.ENTRY_CD    = /*entryCd*/'00'
AND   BR18.ENTRY_YEAR  = /*entryYear*/'9999'
AND   BR18.ENTRY_KAI   = /*entryKai*/'001'
AND   BR18.USERTYPE_CD = /*usertypeCd*/''
AND   BR18.FROM_DT    <= /*sysDate*/
AND   BR18.TO_DT      >= /*sysDate*/
AND   BR18.DAY_KBN     = '06'
AND   BR17.ENTRY_CD    = BC10.ENTRY_CD
AND   BR18.ENTRY_CD    = BR17.ENTRY_CD
AND   BR18.ENTRY_YEAR  = BR17.ENTRY_YEAR
AND   BR18.ENTRY_KAI   = BR17.ENTRY_KAI
AND   BR19.ENTRY_CD    = BR18.ENTRY_CD
AND   BR19.ENTRY_YEAR  = BR18.ENTRY_YEAR 
AND   BR19.ENTRY_KAI   = BR18.ENTRY_KAI 

group by BR19.COURSE_CD
,	     BR19.COURSE_NAME

order by BR19.COURSE_CD