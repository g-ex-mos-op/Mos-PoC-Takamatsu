SELECT 
    BS02.COMPANY_CD
   ,BS02.URIKAKE_CD
   ,rtrim(TM11.ONER_NAME_KJ) as ONER_NAME_KJ
   ,BS02.HAKKO_DT
   ,BS02.SEIKYUSHO_ID
   ,BS02.SEIKYU_ZEN
   ,BS02.NYUKIN_GAKU
   ,BS02.KURI_ZAN
   ,BS02.KAZEI_GAKU
   ,BS02.SHO_TAX
   ,BS02.HAND_TAX
   ,(BS02.SHO_TAX + BS02.HAND_TAX)  as TAX_KEI
   ,BS02.HIZEI_GAKU
   ,BS02.SEIKYU_KON
   ,BS02.NYUKIN_RUISEKI
   ,(BS02.KAZEI_GAKU + BS02.HIZEI_GAKU + BS02.SHO_TAX + BS02.HAND_TAX) as KAIAGE_GAKU 
FROM 
    BS02SEKR BS02
   ,BM33ONUR BM33 
        LEFT OUTER JOIN TM11ONER TM11 ON (BM33.COMPANY_CD = TM11.COMPANY_CD AND BM33.URIKAKE_CD = TM11.ONER_CD)
WHERE
      BS02.COMPANY_CD =BM33.COMPANY_CD
  AND BS02.URIKAKE_CD =BM33.URIKAKE_CD
  AND BS02.COMPANY_CD = /*companyCd*/'00'
  AND BM33.ONER_CD = /*onerCd*/'36056'
  AND substr(BS02.SEIKYUSHO_ID,1,6) >= /*startYm*/'200601'
  AND substr(BS02.SEIKYUSHO_ID,1,6) <= /*endYm*/'200612'
  AND (
       BS02.SEIKYU_ZEN != 0
    OR BS02.NYUKIN_GAKU != 0
    OR BS02.KURI_ZAN != 0
    OR BS02.KAZEI_GAKU != 0
    OR BS02.SHO_TAX != 0
    OR BS02.HAND_TAX != 0
    OR BS02.HIZEI_GAKU != 0
    OR BS02.SEIKYU_KON != 0
    OR BS02.NYUKIN_RUISEKI != 0
      )
ORDER BY
    BS02.URIKAKE_CD
   ,BS02.SEIKYUSHO_ID