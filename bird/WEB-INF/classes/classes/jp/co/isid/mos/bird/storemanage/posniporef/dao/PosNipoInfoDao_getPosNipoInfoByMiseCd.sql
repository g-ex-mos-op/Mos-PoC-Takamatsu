SELECT
    BT63.COMPANY_CD 
   ,BT63.MISE_CD 
   ,BM01.MISE_NAME_KJ as MISE_NAME 
   ,BT63.EIGYO_DT 
   ,BT63.URIAGE 
   ,BT63.SIIRE_SHOKU 
   ,BT63.SIIRE_YASAI 
   ,BT63.SIIRE_HOUZAI 
   ,BT63.SIIRE_OTHER 
   ,BT63.SIIRE_SHOUMOU 
   ,BT63.JITU_SHOKU
   ,BT63.JITU_YASAI
   ,BT63.JITU_HOUZAI
   ,BT63.JITU_OTHER
   ,BT63.STD_SHOKU
   ,BT63.STD_YASAI
   ,BT63.STD_HOUZAI
   ,BT63.STD_OTHER
   ,BT63.PA_SAL
   ,BT63.PA_WH
   ,BT63.SHAIN_SAL
   ,BT63.SHAIN_WH
FROM
    BT63SNIP BT63 
    LEFT JOIN BM01TENM BM01 ON (BT63.COMPANY_CD = BM01.COMPANY_CD and BT63.MISE_CD = BM01.MISE_CD )
WHERE
      BT63.COMPANY_CD = /*companyCd*/'00'
  AND BT63.MISE_CD    = /*miseCd*/'00744'
  AND BT63.EIGYO_DT >=  /*startYmd*/'20061201'
  AND BT63.EIGYO_DT <=  /*endYmd*/'20061231'

  /*IF limitFlg==true */
    AND BT63.MISE_CD IN ( 
        SELECT 
            BM50.MISE_CD
        FROM
            BM50TANM BM50 
        WHERE
            BM50.COMPANY_CD = /*companyCd*/'00' AND
            BM50.SV_CD = /*userId*/'00000121'
        GROUP BY
            BM50.MISE_CD 
        )
  /*END*/

ORDER BY 
    BT63.EIGYO_DT
