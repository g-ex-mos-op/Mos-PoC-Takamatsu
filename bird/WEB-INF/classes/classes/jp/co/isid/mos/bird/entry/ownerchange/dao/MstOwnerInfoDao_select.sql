SELECT
    BM01.COMPANY_CD,
    BM01.MISE_CD,
    BM01.ONER_CD,
    BM11.ONER_NAME_KJ
FROM
    BM01TENM as BM01
    inner join BM11ONER as BM11 on BM01.COMPANY_CD = BM11.COMPANY_CD AND
    							   BM01.ONER_CD = BM11.ONER_CD
WHERE
    BM01.COMPANY_CD = /*companyCd*/'00' AND
    BM01.MISE_CD = /*miseCd*/'01374'
