SELECT BM07.USER_ID
,      BM07.COMPANY_CD
,      BM07.MISE_CD
,      br01.USER_NAME_KJ
,      BR01.USER_NAME_KANA
,      BR01.USERTYPE_CD
,      BR01.USER_PSWD

FROM BM07UTEN BM07
,    BR01USER BR01 

WHERE BM07.COMPANY_CD  = /*companyCd*/'00'
AND   BM07.MISE_CD     = /*miseCd*/'02001'
AND   BR01.STOP_FLG    <> '1'
AND   BR01.SEKYU_FLG   = '1'
AND   BR01.USERTYPE_CD = '03'
AND   BR01.USER_ID     = BM07.USER_ID