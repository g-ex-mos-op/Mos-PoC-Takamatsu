    BM01TENM.COMPANY_CD,
    BM01TENM.MISE_CD,
    BM01TENM.MISE_NAME_KJ,
    BM01TENM.MISE_NAME_KNA,
    (CASE WHEN BM01TENM.CLOSE_DT <= BR33.CNT_DATE THEN BM01TENM.CLOSE_DT ELSE '' END) AS CLOSE_KJ
