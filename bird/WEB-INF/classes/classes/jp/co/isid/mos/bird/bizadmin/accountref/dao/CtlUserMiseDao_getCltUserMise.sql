SELECT
	BM07.USER_ID,
	BM07.COMPANY_CD,
	BC05.COMPANY_NAME,
	BM07.MISE_CD,
	BM01TENM .MISE_NAME_KJ,
	BM07.FIRST_USER,
	BM07.FIRST_PGM,
	BM07.FIRST_TMSP,
	BM07.LAST_USER,
	BM07.LAST_PGM,
	BM07.LAST_TMSP
FROM BM07UTEN BM07
LEFT JOIN BC05KCOM BC05
     ON (BC05.COMPANY_CD = BM07.COMPANY_CD)
LEFT JOIN BM01TENM 
     ON (BM07.COMPANY_CD = BM01TENM.COMPANY_CD
         AND BM07.MISE_CD = BM01TENM.MISE_CD )
	
WHERE BM07.USER_ID = /*user_id*/'99990003'