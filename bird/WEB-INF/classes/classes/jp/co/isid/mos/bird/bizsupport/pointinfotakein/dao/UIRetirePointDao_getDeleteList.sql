SELECT
	BD61.NENDO,
	RTRIM(BD61.USER_ID) AS USER_ID,
	BD61.POINT_RUIKEI,
	BD61.KB_COMPANY_CD,
	BD61.NYUSYA_DT,
	BD61.TAISHOKU_DT,
	BD61.KINZOKU_YEAR,
	BD61.RETIRE_CD,
	BD61.SIKYU_RATE,
	BD61.T_SEISAN_POINT,
	RTRIM(BD61.BIKOU) AS BIKOU,
	BD61.FIRST_USER,
	BD61.FIRST_PGM,
	BD61.FIRST_TMSP,
	BD61.LAST_USER,
	BD61.LAST_PGM,
	BD61.LAST_TMSP

FROM
    BD61TSES BD61
    INNER JOIN BD64TSES BD64
        ON (BD64.NENDO = BD61.NENDO
            AND BD64.USER_ID = BD61.USER_ID
            AND BD64.TAISHOKU_DT = BD61.TAISHOKU_DT
            AND BD64.NYUSYA_DT = BD61.NYUSYA_DT)

WHERE BD64.TORI_SAKUJO_FLG = '1'
    AND BD64.TMSP = /*tmsp*/'2017-02-23 17:41:09.144000'