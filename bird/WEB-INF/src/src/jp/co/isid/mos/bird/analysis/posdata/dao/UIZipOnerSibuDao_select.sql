SELECT distinct rtrim(BM10.SIBU_CD) as  SIBU_CD
,      rtrim(BM10.SIBU_NAME) as SIBU_NAME

FROM   BC36ZIPO BC36
,      BM10GSIB BM10
,      BM01TENM BM01

WHERE  BM01.COMPANY_CD = /*companyCd*/'00'
AND    BM01.ONER_CD    = /*onerCd*/'37006'
AND    BC36.COMPANY_CD = BM01.COMPANY_CD
AND    BC36.ONER_CD    = BM01.ONER_CD
AND    BM10.COMPANY_CD = BM01.COMPANY_CD
AND    BM10.SIBU_CD    = BM01.SIBU_CD

ORDER BY SIBU_CD