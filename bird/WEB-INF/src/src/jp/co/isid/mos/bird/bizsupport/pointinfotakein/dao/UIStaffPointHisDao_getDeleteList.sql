SELECT
	BD59.NENDO,
	RTRIM(BD59.USER_ID) AS USER_ID,
	BD59.POINT,
	BD59.POINT_SHU,
	BD59.KB_COMPANY_CD,
	BD59.RANK_CD,
	RTRIM(BD59.KAIGAI_FLG) AS KAIGAI_FLG,
	RTRIM(BD59.BIKOU) AS BIKOU,
	BD59.FIRST_USER,
	BD59.FIRST_PGM,
	BD59.FIRST_TMSP,
	BD59.LAST_USER,
	BD59.LAST_PGM,
	BD59.LAST_TMSP

FROM
    BD59HPRI BD59
    INNER JOIN BD62HPRI BD62
        ON (BD62.NENDO = BD59.NENDO
            AND BD62.USER_ID = BD59.USER_ID
            AND BD62.POINT_SHU = BD59.POINT_SHU)

WHERE BD59.POINT_SHU = /*pointShu*/'1'
	AND BD62.TORI_SAKUJO_FLG = '1'
    AND BD62.TMSP = /*tmsp*/'2017-02-23 17:41:09.144000'