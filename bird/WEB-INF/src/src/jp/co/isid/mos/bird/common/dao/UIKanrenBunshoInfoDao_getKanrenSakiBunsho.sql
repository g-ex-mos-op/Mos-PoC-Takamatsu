select 
  BT11.INFO_SHU
, BT11.REG_DATE
, BT11.SEQ
, BT11.REL_INFO_SHU
, BT11.REL_REG_DATE
, BT11.REL_SEQ
, BT01.TITLE       AS    BT01_INFO_TITLE     
, BT02.TITLE       AS    BT02_INFO_TITLE     
, BT03.TITLE       AS    BT03_INFO_TITLE     
, BT04.TITLE       AS    BT04_INFO_TITLE     
, BT05.TITLE       AS    BT05_INFO_TITLE     
, BT11.FIRST_USER
, BT11.FIRST_PGM
, BT11.FIRST_TMSP
, BT11.LAST_USER
, BT11.LAST_PGM
, BT11.LAST_TMSP
from 
  BT11RLFL BT11
    left outer join BT01INFM BT01 on ( BT01.REG_DATE = BT11.REG_DATE and BT01.SEQ = BT11.SEQ ) 
    left outer join BT02NTCM BT02 on ( BT02.REG_DATE = BT11.REG_DATE and BT02.SEQ = BT11.SEQ ) 
    left outer join BT03DOCM BT03 on ( BT03.REG_DATE = BT11.REG_DATE and BT03.SEQ = BT11.SEQ )
    left outer join BT04FRMM BT04 on ( BT04.REG_DATE = BT11.REG_DATE and BT04.SEQ = BT11.SEQ )
    left outer join BT05WNEW BT05 on ( BT05.REG_DATE = BT11.REG_DATE and BT05.SEQ = BT11.SEQ )
where 
       BT11.REL_INFO_SHU = /*relInfoShu*/'03'
AND    BT11.REL_REG_DATE = /*relRegDate*/'20060601'
AND    BT11.REL_SEQ      = /*relSeq*/'0000'
