SELECT DISTINCT
       BM41.MENU_CD as MENU_CD
  ,    rtrim(MENU_NAME_KJ) as MENU_NAME_KJ
FROM BM41CMNU BM41
 INNER JOIN BM38MMNU BM38
  ON (BM41.MENU_CD = BM38.MENU_CD) 
 INNER JOIN BM40CMGP BM40
  ON (BM41.MENU_GROUP = BM40.MENU_GROUP) 
WHERE BM41.CKANRI_NO = /*ckanriNo*/'200601'
AND   BM38.MISE_CD = /*miseCd*/'01057'
ORDER BY BM41.MENU_CD
