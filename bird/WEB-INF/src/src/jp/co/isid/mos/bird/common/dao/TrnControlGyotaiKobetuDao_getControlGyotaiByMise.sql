select distinct
    BT15.INFO_SHU,
    BT15.REG_DATE,
    BT15.SEQ,
    BT15.GYOTAI_KBN
from
    BT15IAID BT15,
    BM04GTCP BM04,
    BM05USGT BM05,
    BM07UTEN BM07,
    BM01TENM BM01
where
    BT15.INFO_SHU = /*infoShu*/'03' 
and BT15.KOBETSU_SHU = '01'
and BM05.USER_ID = /*userId*/'99990003'
and BM07.USER_ID = /*userId*/'99990003'
and BT15.GYOTAI_KBN = BM05.GYOTAI_KBN
and BT15.GYOTAI_KBN = BM04.GYOTAI_KBN
and BM04.COMPANY_CD = BM07.COMPANY_CD
and BM07.COMPANY_CD = BM01.COMPANY_CD
and BM01.MISE_CD = BM07.MISE_CD
and BT15.KOBETSU_CD = BM01.AREA_DAI
group by
    BT15.INFO_SHU,
    BT15.REG_DATE,
    BT15.SEQ,
    BT15.GYOTAI_KBN
order by 
    BT15.REG_DATE,
    BT15.SEQ