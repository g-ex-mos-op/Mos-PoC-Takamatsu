SELECT
  BD65.EIGYO_DT
  , SUM(BD65.URIAGE1) AS URIAGE1
  , SUM(BD65.URIAGE2) AS URIAGE2
  , SUM(BD65.URIAGE3) AS URIAGE3
  , SUM(BD65.URIAGE4) AS URIAGE4
  , SUM(BD65.URIAGE5) AS URIAGE5
  , SUM(BD65.URIAGE1 + BD65.URIAGE2 + BD65.URIAGE3 + BD65.URIAGE4 + BD65.URIAGE5) AS TOTAL_URIAGE
  , SUM(BD65.TAX1) AS TAX1
  , SUM(BD65.TAX2) AS TAX2
  , SUM(BD65.TAX3) AS TAX3
  , SUM(BD65.TAX4) AS TAX4
  , SUM(BD65.TAX5) AS TAX5
  , SUM(BD65.TAX1 + BD65.TAX2 + BD65.TAX3 + BD65.TAX4 + BD65.TAX5) AS TOTAL_TAX
FROM
  BD65SADM BD65
WHERE
  BD65.COMPANY_CD = /*companyCd*/'00'
  AND BD65.EIGYO_DT LIKE /*taishoYM*/'201807' || '%'
  AND BD65.MISE_CD = /*taishoTenpoCd*/'04413'
GROUP BY
  ROLLUP (BD65.EIGYO_DT)
ORDER BY
  BD65.EIGYO_DT