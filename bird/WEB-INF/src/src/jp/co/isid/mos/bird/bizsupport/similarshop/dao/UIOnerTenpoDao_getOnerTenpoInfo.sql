select
	BM01.COMPANY_CD,
	BM01.MISE_CD,
	BM01.AREA_DAI,
	rtrim(BM01.MISE_NAME_KJ) as MISE_NAME_KJ,
	BM01.ONER_CD,
	BM01.OPEN_DT,
	BM01.LOCATE_KBN,
	BM01.MISE_KEITAI,
	(case when BT17.URIAGEDAKA is null
	      then 0 else BT17.URIAGEDAKA end) as URIAGEDAKA,
	BM01.CLOSE_DT
	
from
	BT17PLDT BT17,
	BM01TENM BM01

where
	BM01.MISE_CD = BT17.MISE_CD and
	BM01.COMPANY_CD = BT17.COMPANY_CD and
	BM01.COMPANY_CD = '00' and
	BT17.PL_YM = /*zenMonthYYYYMM*/'200601' and
	BM01.ONER_CD = /*onerCd*/'36001' and
	BT17.PL_TYPE = '1' and
	BT17.ERR_FLG = '0' 
	
order by BT17.MISE_CD