select BM01.MISE_CD,
       rtrim(BM01.MISE_NAME_KJ) as MISE_NAME_KJ,
       BM01.SIBU_CD,
       rtrim(BM10.SIBU_NAME) as SIBU_NAME,
       rtrim(BC09.GYOTAI_KBN_NAME) as GYOTAI_KBN_NAME 
from BC09GTAI BC09,
     BM04GTCP BM04,
     BM01TENM BM01,
     BM10GSIB BM10
where 
    BM01.COMPANY_CD = BM04.COMPANY_CD 
and BM01.MISE_CD = /*miseCd*/'01776' 
and BM04.GYOTAI_KBN = /*gyotaiKbn*/'013' 
and BM04.GYOTAI_KBN = BM01.MISE_GRP1 
and BM04.GYOTAI_KBN = BC09.GYOTAI_KBN 
and BM01.COMPANY_CD = BM10.COMPANY_CD 
and BM01.SIBU_CD = BM10.SIBU_CD