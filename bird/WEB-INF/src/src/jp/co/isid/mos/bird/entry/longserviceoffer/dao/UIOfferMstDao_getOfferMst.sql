SELECT
    BT21ENOJ.ENTRY_CD,
    BT21ENOJ.ENTRY_YEAR,
    BT21ENOJ.ENTRY_KAI,
    BT21ENOJ.COMPANY_CD,
    BT21ENOJ.ONER_CD,
    BM11ONER.ONER_NAME_KJ,
    BT21ENOJ.ATESAKI_KBN,
    BT21ENOJ.NAME,
    BT21ENOJ.TEL,
    BT21ENOJ.ZIP,
    BT21ENOJ.ADDRESS1,
    BT21ENOJ.ADDRESS2,
    BT21ENOJ.ADDRESS3,
    BT21ENOJ.SOUFU_NAME,
    BT21ENOJ.JOB_TYPE,
    BT21ENOJ.FIRST_USER,
    BT21ENOJ.FIRST_PGM,
    BT21ENOJ.FIRST_TMSP,
    BT21ENOJ.LAST_USER,
    BT21ENOJ.LAST_PGM,
    BT21ENOJ.LAST_TMSP
FROM BT21ENOJ
JOIN BM11ONER AS BM11ONER ON (
	BM11ONER.COMPANY_CD = /*companyCd*/'00' AND
	BM11ONER.ONER_CD = /*onerCd*/'36478')
WHERE 
	BT21ENOJ.ENTRY_CD = /*entryCd*/'00' AND
    BT21ENOJ.ENTRY_YEAR = /*entryYear*/'2007' AND
    BT21ENOJ.ENTRY_KAI = /*entryKai*/'001' AND
    BT21ENOJ.COMPANY_CD = /*companyCd*/'00' AND
    BT21ENOJ.ONER_CD = /*onerCd*/'36478' AND
	BT21ENOJ.ATESAKI_KBN = /*atesakiKbn*/'00'
