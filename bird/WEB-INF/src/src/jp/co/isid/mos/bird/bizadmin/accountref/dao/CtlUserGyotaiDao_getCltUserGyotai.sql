SELECT
	BM05USGT .USER_ID,
	BM05USGT .GYOTAI_KBN,
	BC09GTAI .GYOTAI_KBN_NAME,
	BM05USGT .FIRST_USER,
	BM05USGT .FIRST_PGM,
	BM05USGT .FIRST_TMSP,
	BM05USGT .LAST_USER,
	BM05USGT .LAST_PGM,
	BM05USGT .LAST_TMSP

FROM 
    BM05USGT LEFT JOIN 
    BC09GTAI ON (BM05USGT.GYOTAI_KBN= BC09GTAI.GYOTAI_KBN)

WHERE
	BM05USGT .USER_ID = /*user_id*/'99990002'
ORDER BY 
	BM05USGT .USER_ID