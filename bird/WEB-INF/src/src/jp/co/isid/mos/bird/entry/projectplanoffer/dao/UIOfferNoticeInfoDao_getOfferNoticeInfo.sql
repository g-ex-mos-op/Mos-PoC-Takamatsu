select
	LTRIM(RTRIM(BR51.NOTICE1)) as NOTICE1,
	LTRIM(RTRIM(BR17.NOTE)) as NOTE
from
	BR51ENCI as BR51,
	BR17ENTL as BR17
where
	BR51.ENTRY_CD = /*entryCd*/'25' AND
	BR51.ENTRY_YEAR = /*entryYear*/'2006' AND
	BR51.ENTRY_KAI = /*entryKai*/'999' AND
	BR51.ENTRY_CD = BR17.ENTRY_CD  AND
	BR51.ENTRY_YEAR = BR17.ENTRY_YEAR AND
	BR51.ENTRY_KAI = BR17.ENTRY_KAI