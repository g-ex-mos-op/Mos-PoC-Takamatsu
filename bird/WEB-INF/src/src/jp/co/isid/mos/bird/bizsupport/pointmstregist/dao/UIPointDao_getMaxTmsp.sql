SELECT
    BM77.TOUROKU_NO,
	BM77.TOUROKU_SEQ,
	BM77.DATA_VER,
    BM77.NENDO,
    BM77.KB_COMPANY_CD,
    BM77.RANK_CD,
    BM77.KIHON_POINT,
    BM77.LAST_TMSP

FROM BM77HUYP BM77

WHERE BM77.TOUROKU_NO = /*tourokuNo*/'0001'
    AND BM77.SAKUJO_FLG = '0'

ORDER BY BM77.LAST_TMSP DESC
FETCH FIRST 1 ROW ONLY