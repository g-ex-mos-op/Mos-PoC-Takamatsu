select distinct
    BT16.INFO_SHU,
    BT16.REG_DATE,
    BT16.SEQ,
    BT16.GYOTAI_KBN
from
    BT16IAST BT16,
    BM04GTCP BM04,
    BM05USGT BM05,
    BM07UTEN BM07
where
    BT16.INFO_SHU = /*infoShu*/'03' 
and BM05.USER_ID = /*userId*/'99990003'
and BM07.USER_ID = /*userId*/'99990003'
and BT16.GYOTAI_KBN = BM05.GYOTAI_KBN
and BT16.GYOTAI_KBN = BM04.GYOTAI_KBN
and BT16.MISE_CD = BM07.MISE_CD
and BM04.COMPANY_CD = BM07.COMPANY_CD
group by
    BT16.INFO_SHU,
    BT16.REG_DATE,
    BT16.SEQ,
    BT16.GYOTAI_KBN
order by 
    BT16.REG_DATE,
    BT16.SEQ