SELECT DISTINCT
    BT05WNEW.REG_DATE,
    BT05WNEW.SEQ,
    BT05WNEW.TITLE,
    BT05WNEW.PUB_DATE_FROM,
    BT05WNEW.PUB_DATE_TO,
    BT05WNEW.PUB_USER,
    BT05WNEW.PUB_ORG,
    BT05WNEW.PUB_ORG_NAME,
    BT05WNEW.SIMPLE_MSG,
    BT05WNEW.MESSAGE,
    BT14IAGT.GYOTAI_KBN,
    BT14IAGT.KOBETSU_FLG,
    BT14IAGT.MISE_FLG
FROM
    BT05WNEW
    inner join BT12IACP on BT05WNEW.REG_DATE = BT12IACP.REG_DATE
   	                 and BT05WNEW.SEQ = BT12IACP.SEQ
   	                 and BT12IACP.INFO_SHU = '05'
    inner join BT13IASZ on BT05WNEW.REG_DATE = BT13IASZ.REG_DATE
                       and BT05WNEW.SEQ = BT13IASZ.SEQ
   	                 and BT13IASZ.INFO_SHU = '05'
    inner join BT14IAGT on BT05WNEW.REG_DATE = BT14IAGT.REG_DATE
     	                 and BT05WNEW.SEQ = BT14IAGT.SEQ
   	                 and BT14IAGT.INFO_SHU = '05'
    inner join BM03USCP on BM03USCP.R_COMPANY_CD = BT12IACP.R_COMPANY_CD
    inner join BM13SHKM on BM13SHKM.SHOZOKU_KBN = BT13IASZ.SHOZOKU_KBN
    inner join BM05USGT on BM05USGT.GYOTAI_KBN = BT14IAGT.GYOTAI_KBN
WHERE
    BT05WNEW.SAKUJO_FLG <> '1'
AND BM03USCP.USER_ID = /*USER_ID*/'99990003'
AND BM13SHKM.USER_ID = /*USER_ID*/'99990003'
AND BM05USGT.USER_ID = /*USER_ID*/'99990003'
AND BT05WNEW.PUB_DATE_FROM <= /*SYS_DATE*/'20060201' 
AND BT05WNEW.PUB_DATE_TO >= /*SYS_DATE*/'20060201' 
ORDER BY BT05WNEW.PUB_DATE_FROM DESC, BT05WNEW.REG_DATE DESC, BT05WNEW.SEQ DESC