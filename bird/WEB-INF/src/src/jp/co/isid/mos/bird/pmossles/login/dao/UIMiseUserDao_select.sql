SELECT BM07.USER_ID
,      BM07.COMPANY_CD
,      BM07.MISE_CD
,      BR01.USER_NAME_KJ
,      BR01.USER_NAME_KANA
,      BR01.USERTYPE_CD
,      BR01.USER_PSWD

FROM BM07UTEN BM07
,    BR01USER BR01
,    BM01TENM BM01


WHERE BM07.COMPANY_CD  = /*companyCd*/'00'
AND   BM07.MISE_CD     = /*miseCd*/'02001'
AND   BR01.USER_ID     = BM07.USER_ID
AND   BM07.COMPANY_CD  = BM01.COMPANY_CD
AND   BM07.MISE_CD     = BM01.MISE_CD
AND   BR01.USERTYPE_CD = '03'
AND ((BM01.MISE_CD  NOT LIKE '09%'
AND   BM01.CLOSE_DT   >= (SELECT  BR33.CNT_DATE FROM BR33CDAY BR33 WHERE BR33.DAY_KBN     = '02'))
OR   (BM01.MISE_CD      LIKE '09%'))
AND   SUBSTR(BM07.USER_ID,1,1) =
      CASE WHEN SUBSTR(BM01.ONER_CD,1,2) = '36' THEN 'A'
           WHEN SUBSTR(BM01.ONER_CD,1,2) = '37' THEN 'B'
           WHEN SUBSTR(BM01.ONER_CD,1,2) = '38' THEN 'C'
           WHEN SUBSTR(BM01.ONER_CD,1,2) = '39' THEN 'D'
      END
AND   SUBSTR(BM07.USER_ID,2,1) = SUBSTR(BM01.ONER_CD,5,1)
AND   SUBSTR(BM07.USER_ID,3,1) =
      CASE WHEN SUBSTR(BM01.ONER_CD,3,1) = '0' THEN 'A'
           WHEN SUBSTR(BM01.ONER_CD,3,1) = '1' THEN 'B'
           WHEN SUBSTR(BM01.ONER_CD,3,1) = '2' THEN 'C'
           WHEN SUBSTR(BM01.ONER_CD,3,1) = '3' THEN 'D'
           WHEN SUBSTR(BM01.ONER_CD,3,1) = '4' THEN 'E'
           WHEN SUBSTR(BM01.ONER_CD,3,1) = '5' THEN 'F'
           WHEN SUBSTR(BM01.ONER_CD,3,1) = '6' THEN 'G'
           WHEN SUBSTR(BM01.ONER_CD,3,1) = '7' THEN 'H'
           WHEN SUBSTR(BM01.ONER_CD,3,1) = '8' THEN 'J'
           WHEN SUBSTR(BM01.ONER_CD,3,1) = '9' THEN 'K'
      END
AND   SUBSTR(BM07.USER_ID,4,1) =
      CASE WHEN SUBSTR(BM01.ONER_CD,4,1) = '0' THEN 'L'
           WHEN SUBSTR(BM01.ONER_CD,4,1) = '1' THEN 'M'
           WHEN SUBSTR(BM01.ONER_CD,4,1) = '2' THEN 'N'
           WHEN SUBSTR(BM01.ONER_CD,4,1) = '3' THEN 'P'
           WHEN SUBSTR(BM01.ONER_CD,4,1) = '4' THEN 'Q'
           WHEN SUBSTR(BM01.ONER_CD,4,1) = '5' THEN 'R'
           WHEN SUBSTR(BM01.ONER_CD,4,1) = '6' THEN 'S'
           WHEN SUBSTR(BM01.ONER_CD,4,1) = '7' THEN 'T'
           WHEN SUBSTR(BM01.ONER_CD,4,1) = '8' THEN 'U'
           WHEN SUBSTR(BM01.ONER_CD,4,1) = '9' THEN 'V'
      END
AND   SUBSTR(BM07.USER_ID,5,4) = SUBSTR(BM01.MISE_CD,2,4)
