select 
	rtrim(CT01.MISE_CD) as MISE_CD,
	rtrim(BM01.MISE_NAME_KJ) as MISE_NAME_KJ,
	rtrim(CT01.SHO_CD_DAI) as SHO_CD_DAI,
	rtrim(CM10.POS_NAME_KNA) as POS_NAME_KNA,
	CM04.TANKA_URI,
	CM10.YU_MUSHO_KBN,
	CT04.SHO_AMOUNT,
	CT01.MUSHO_E_AMT,
	CT01.YUSHO_E_AMT,
	CT01.YUSHO_TUIKA,
	CM10.POS_NISUGATA,
	CM10.POS_IRIME
from
	CT01JMST as CT01,
	BM01TENM as BM01,
	CM10SHOP as CM10,
	CM04SHOM as CM04,
	CT04PTRN as CT04
where
    CT01.COMPANY_CD = /*companyCd*/'00' and
    CT01.CMP_NO = /*cmpNo*/'' and
    CT01.COMPANY_CD = BM01.COMPANY_CD and
    CT01.MISE_CD = BM01.MISE_CD and
	BM01.OPEN_DT <= /*sysDate*/'20060823' and    
	BM01.CLOSE_DT >= /*sysDate*/'20060823' and
    CT01.CMP_NO = CM10.CMP_NO and
    CT01.SHO_CD_DAI = CM10.SHO_CD_DAI and
    CT01.CMP_NO = CM04.CMP_NO and
    CT01.SHO_CD_DAI = CM04.SHO_CD_DAI and
    CT01.COMPANY_CD = CT04.COMPANY_CD and
    CT01.CMP_NO = CT04.CMP_NO and
    CT01.MISE_CD = CT04.MISE_CD and
    CT01.SHO_CD_DAI = CT04.SHO_CD_DAI and
	CT01.MISE_CD in (
			SELECT
			    MISE_CD
			FROM 
		    	BM01TENM as BM01_2
			WHERE
		    	COMPANY_CD = /*companyCd*/'00' and
		    	ONER_CD in /*onerCdList*/('36961','36478')
				and CLOSE_DT > /*sysDate*/'200605'
			ORDER BY MISE_CD)
order by MISE_CD, SHO_CD_DAI