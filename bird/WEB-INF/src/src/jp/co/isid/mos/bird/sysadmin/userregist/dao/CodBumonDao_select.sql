SELECT
   BC08.R_COMPANY_CD
  ,BC08.BUMON_CD
  ,BC08.BUMON_CD concat '  ' concat rtrim(BC08.BUMON_NAME) as BUMON_NAME
FROM
    BM03USCP BM03 INNER JOIN BC08CBMN BC08 
      ON BM03.R_COMPANY_CD=BC08.R_COMPANY_CD 
WHERE
BC08.R_COMPANY_CD = /*R_COMPANY_CD*/'9999999999'
GROUP BY BC08.R_COMPANY_CD,BC08.BUMON_CD,BC08.BUMON_NAME
ORDER BY BC08.R_COMPANY_CD,BC08.BUMON_CD       