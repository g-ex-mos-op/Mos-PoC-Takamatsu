SELECT
	BD59.NENDO,
	RTRIM(BD59.USER_ID) AS USER_ID,
	BD59.POINT,
	BD59.POINT_SHU,
	BD59.KB_COMPANY_CD,
	BD59.RANK_CD,
	RTRIM(BD59.KAIGAI_FLG) AS KAIGAI_FLG,
	RTRIM(BD59.BIKOU) AS BIKOU,
	BD59.FIRST_USER,
	BD59.FIRST_PGM,
	BD59.FIRST_TMSP,
	BD59.LAST_USER,
	BD59.LAST_PGM,
	BD59.LAST_TMSP

FROM
    BD59HPRI BD59
    INNER JOIN BD64TSES BD64
        ON (BD64.NENDO = BD59.NENDO
            AND BD64.USER_ID = BD59.USER_ID)

WHERE BD59.POINT_SHU = /*pointShu*/'1'
	AND BD64.TORI_SAKUJO_FLG = '1'
    AND BD64.TMSP = /*tmsp*/'2017-02-23 17:41:09.144000'