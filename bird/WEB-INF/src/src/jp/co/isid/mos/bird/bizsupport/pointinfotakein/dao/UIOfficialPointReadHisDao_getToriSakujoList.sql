SELECT
	BD63.TMSP,
	BD63.NENDO,
	RTRIM(BD63.USER_ID) AS USER_ID,
	BD63.TORI_SAKUJO_FLG,
	BD63.POINT,
	BD63.POINT_SHU,
	RTRIM(BD63.POINT_SHU_NAME) AS POINT_SHU_NAME,
	BD63.KB_COMPANY_CD,
	RTRIM(BD63.BIKOU) AS BIKOU,
	BD63.FIRST_USER,
	BD63.FIRST_PGM,
	BD63.FIRST_TMSP,
	BD63.LAST_USER,
	BD63.LAST_PGM,
	BD63.LAST_TMSP

FROM
    BD63YPRI BD63
    LEFT OUTER JOIN BD60YPRI BD60
        ON (BD63.NENDO = BD60.NENDO
            AND BD63.USER_ID = BD60.USER_ID
            AND BD63.POINT_SHU_NAME = BD60.POINT_SHU_NAME)

WHERE BD63.TMSP = /*tmsp*/'2017-02-23 17:41:09.144000'
    AND BD63.TORI_SAKUJO_FLG = '1'
	AND BD60.NENDO IS NULL