SELECT DISTINCT BM10.SLAREA_CD as  HYOJITAISHO_CD
,      rtrim(BM10.SLAREA_NAME) as  HYOJITAISHO_NAME
FROM BM10GSIB BM10
,    BM01TENM as BM01
,    BM65CPMS BM65
/*IF userTypeCd == "01" && limitFlg */
,	 BM51SVSB BM51
/*END*/
/*IF userTypeCd == "02" */
,    BM06UONR BM06
/*END*/
/*IF userTypeCd == "03" */
,    BM07UTEN BM07
/*END*/
WHERE BM01.COMPANY_CD = /*companyCd*/'00'
/*IF userTypeCd == "01" && limitFlg */
AND   BM01.SIBU_CD IN
        (SELECT BM51.SIBU_CD
         FROM BM51SVSB BM51
         WHERE
           BM51.COMPANY_CD = /*companyCd*/'00' AND
           BM51.SV_CD = /*userId*/'99990005'
        )
/*END*/
/*IF userTypeCd == "02" */
AND   BM06.USER_ID    = /*userId*/'99990003'
AND   BM06.COMPANY_CD = BM01.COMPANY_CD
AND   BM06.ONER_CD    = BM01.ONER_CD
/*END*/
/*IF userTypeCd == "03" */
AND   BM07.USER_ID    = /*userId*/'99990003'
AND   BM07.COMPANY_CD = BM01.COMPANY_CD
AND   BM07.MISE_CD    = BM01.MISE_CD
/*END*/
AND   BM01.COMPANY_CD = BM65.COMPANY_CD
AND   BM01.MISE_CD = BM65.MISE_CD
AND   BM65.CAMP_ID = /*campId*/'200806'
AND   BM01.COMPANY_CD = BM10.COMPANY_CD
AND   BM01.SIBU_CD = BM10.SIBU_CD

ORDER BY HYOJITAISHO_CD