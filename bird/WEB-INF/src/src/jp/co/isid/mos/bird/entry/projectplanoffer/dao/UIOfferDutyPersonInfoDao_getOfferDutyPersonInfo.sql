	BT21.ENTRY_CD = /*entryCd*/'25' and
	BT21.ENTRY_YEAR = /*entryYear*/'2006' and 
	BT21.ENTRY_KAI = /*entryKai*/'999' and 
	BT21.COMPANY_CD = /*companyCd*/'00' and
	BT21.ONER_CD = /*onerCd*/'36478' and
	BT21.ATESAKI_KBN = '00' and
	BT21.COMPANY_CD = BM11.COMPANY_CD and
	BT21.ONER_CD = BM11.ONER_CD