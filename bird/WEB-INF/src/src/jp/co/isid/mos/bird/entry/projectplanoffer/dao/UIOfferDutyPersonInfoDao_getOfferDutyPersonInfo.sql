	LTRIM(RTRIM(BM11.ONER_NAME_KJ)) as ONER_NAME_KJ,
	LTRIM(RTRIM(BT21.SOUFU_NAME)) as SOUFU_NAME ,	
	LTRIM(RTRIM(BT21.NAME)) as NAME,
	LTRIM(RTRIM(BT21.TEL)) as TEL,
