select BC10.ENTRY_CD
,      BR17.ENTRY_YEAR
,      BR17.ENTRY_KAI
,      BR17.ENTRY_TITLE
,      BC10.ENTRY_CD CONCAT BR17.ENTRY_YEAR CONCAT BR17.ENTRY_KAI AS TARGET_CD
from BC10ENTM BC10
,    BR17ENTL BR17
,    BR18ENTD BR18
where BC10.ENTRY_TYPE='1'
AND   BC10.ENTRY_CD = /*entrycd*/'30'
AND   BR18.USERTYPE_CD = /*usertypeCd*/'01'
AND   BR18.FROM_DT    <= /*sysDate*/'20070613'
AND   BR18.TO_DT      >= /*sysDate*/'20070613'
AND   BR18.DAY_KBN = '06'
AND   BR17.ENTRY_CD = BC10.ENTRY_CD
AND   BR18.ENTRY_CD = BR17.ENTRY_CD
AND   BR18.ENTRY_YEAR = BR17.ENTRY_YEAR
AND   BR18.ENTRY_KAI = BR17.ENTRY_KAI
AND   BR17.SAKUJO_FLG <> '1'
order by BR17.ENTRY_YEAR DESC
,	     BR17.ENTRY_KAI ASC