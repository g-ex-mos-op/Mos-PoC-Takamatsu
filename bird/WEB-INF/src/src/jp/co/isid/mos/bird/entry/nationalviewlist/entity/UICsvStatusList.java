    /**
     * 性別漢字名称取得処理
     *
     * 0 :　男
     * 1 :  女
     * 空:  ''
     * @return
     */
    public String getSexKj() {
        String sex = getSex();
        if(sex != null){
            if(NationalViewListUtil.SEX_MAN.equals(sex.trim())) {
                return "男";
            }
            else if(NationalViewListUtil.SEX_WOMAN.equals(sex.trim())) {
                return "女";
            }
        }
        return "";
    }
